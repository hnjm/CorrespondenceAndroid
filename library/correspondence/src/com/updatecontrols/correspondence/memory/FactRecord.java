package com.updatecontrols.correspondence.memory;

import com.updatecontrols.correspondence.memento.FactID;

public class FactRecord {
	
	private FactID id;
	private String type;
	private int version;
	private byte[] data;
	private int hashCode;
	private int peerId;
	
	public FactRecord(FactID id, String type, int version, byte[] data, int hashCode, int peerId) {
		this.id = id;
		this.type = type;
		this.version = version;
		this.data = data;
		this.hashCode = hashCode;
		this.peerId = peerId;
	}

	public FactID getId() {
		return id;
	}

	public String getType() {
		return type;
	}

	public int getVersion() {
		return version;
	}

	public byte[] getData() {
		return data;
	}

	public int getHashCode() {
		return hashCode;
	}

	public int getPeerId() {
		return peerId;
	}
	
}
