package com.updatecontrols.correspondence;

public class CorrespondenceException extends Exception {

	private static final long serialVersionUID = 1L;

	public CorrespondenceException() {
		super();
	}

	public CorrespondenceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CorrespondenceException(String arg0) {
		super(arg0);
	}

	public CorrespondenceException(Throwable arg0) {
		super(arg0);
	}

}
